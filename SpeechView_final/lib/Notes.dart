                                    ? "Record"
                                    : "Recording",
                                style: TextStyle(
                                  fontSize: 20.00,
                                  color: Colors.blue,
                                  fontWeight: FontWeight.w500,
                                ),
                              ),
                            ],
                          ),
                        ),
                      ),
                    ],
                  ),
                ),
              ),
            ),
          ),
        );
      },
    );
